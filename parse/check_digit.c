#include "../includes/ft.h"

int		ft_is_nbr(char *s1)
{
	int	i;

	i = 0;
	while (s1[i])
	{
		if ((s1[i] <= 47 || s1[i] >= 58) && s1[i] != 32)
			return (0);
		i++;
	}
	return (1);
}
